generator = cms.EDFilter("Pythia8GeneratorFilter",
    maxEventsToPrint = cms.untracked.int32(1),
    pythiaPylistVerbosity = cms.untracked.int32(1),
    filterEfficiency = cms.untracked.double(1.0),
    pythiaHepMCVerbosity = cms.untracked.bool(False),
    comEnergy = cms.double(13000.),
    RandomizedParameters = cms.VPSet(),
)

model = "ResonantSneu_x0p1"

def events(mx):
  if (mx<800): return 35
  else: return 25

diag = 900
xmin, xmax, xstep = 1500, 3000, 100
ymin, ymax, ystep = 100, 2100, 100 

# -------------------------------
#    Constructing grid

mpoints = []
mpoints.extend([[x,100, events(x)] for x in range(300,1500,100)])
mpoints.extend([[x,200, events(x)] for x in range(500,1500,100)])
mpoints.extend([[x,300, events(x)] for x in range(800,1500,100)])
mpoints.extend([[x,400, events(x)] for x in range(1100,1500,100)])
mpoints.extend([[x,500, events(x)] for x in range(1300,1500,100)])

for mx in range(xmin, xmax+1, xstep):
  ylist = range(ymin, mx-diag+1, ystep)
  for my in ylist:
    mpoints.append([mx,my,events(mx)])

for point in mpoints:
    mslep, mchi = point[0], point[1]
    mchipm = mchi + 0.1*(mslep-mchi)
    qcut, tru_eff = 85, 0.45
    wgt = point[2]

    slhatable = baseSLHATable.replace('%MSLEP%','%e' % mslep)
    slhatable = slhatable.replace('%MCHI%','%e' % mchi)
    slhatable = slhatable.replace('%MCHIPM%','%e' % mchipm)

    basePythiaParameters = cms.PSet(
        pythia8CommonSettingsBlock,
        pythia8CUEP8M1SettingsBlock,
        JetMatchingParameters = cms.vstring(
            'JetMatching:setMad = off',
            'JetMatching:scheme = 1',
            'JetMatching:merge = on',
            'JetMatching:jetAlgorithm = 2',
            'JetMatching:etaJetMax = 5.',
            'JetMatching:coneRadius = 1.',
            'JetMatching:slowJetPower = 1',
            'JetMatching:qCut = %.0f' % qcut, #this is the actual merging scale
            'JetMatching:nQmatch = 5', #4 corresponds to 4-flavour scheme (no matching of b-quarks), 5 for 5-flavour scheme
            'JetMatching:nJetMax = 2', #number of partons in born matrix element for highest multiplicity
            'JetMatching:doShowerKt = off', #off for MLM matching, turn on for shower-kT matching
            'Check:abortIfVeto = on',
        ), 
        parameterSets = cms.vstring('pythia8CommonSettings',
                                    'pythia8CUEP8M1Settings',
                                    'JetMatchingParameters'
        )
    )

    generator.RandomizedParameters.append(
        cms.PSet(
            ConfigWeight = cms.double(wgt),
            GridpackPath =  cms.string('/cvmfs/cms.cern.ch/phys_generator/gridpacks/slc6_amd64_gcc481/13TeV/madgraph/V5_2.3.3/sus_sms/ResonantSneu_x0p1/resSmuSM2_mSlep_%i_mN1_%i_x_0p1_tarball.tar.xz' % (mslep, mchi)),
            ConfigDescription = cms.string('%s_%i_%i' % (model, mslep, mchi)),
            SLHATableForPythia8 = cms.string('%s' % slhatable),
            PythiaParameters = basePythiaParameters,
        ),
    )
